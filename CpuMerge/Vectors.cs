using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CpuMerge
{
    public struct Int2
    {
        public int X, Y;
        public Int2(int x, int y)
        {
            this.X = x;
            this.Y = y;
        }
    }

    public struct Color2
    {
        public int First, Second;
        public Color2(int first, int second)
        {
            this.First = first;
            this.Second = second;
        }
    }
    struct Float2
    {
        public float X, Y;
        public Float2(float x, float y)
        {
            this.X = x;
            this.Y = y;
        }

    }
}
